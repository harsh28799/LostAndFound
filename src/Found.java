    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            try{
                String cat=jComboBox1.getSelectedItem()+"";
                String spec=jTextField1.getText()+","+jTextField2.getText()+","+jTextField3.getText();
                String time=jComboBox2.getSelectedItem()+""+jComboBox3.getSelectedItem();
                String place=jTextField4.getText();

                String q="Insert into FOUND_ITEM values ('FD'||CASEIDFOUND.NEXTVAL,?,?,?,?,?,'A')";
                pat=conn.prepareStatement(q);
                pat.setString(1,jLabel14.getText());
                pat.setString(2,cat);
                pat.setString(3,spec);
                pat.setString(4,time);
                pat.setString(5,place);
                pat.execute();
                JOptionPane.showMessageDialog(null,"NEW CASEID GENERATED!");
                pat.close();
                Status obj=new Status();
                Status.jLabel3.setText(jLabel2.getText());
                this.setVisible(false);
                obj.setVisible(true);
            }catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
    }//GEN-LAST:event_jButton1ActionPerformed

