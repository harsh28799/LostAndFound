    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String roll=jTextField1.getText();
            String password=jTextField2.getText();

            String verify="select * from register where roll=? and password=?";
            pat=conn.prepareStatement(verify);
            pat.setString(1,roll);
            pat.setString(2,password);
            
            rs=pat.executeQuery();
            
            if(rs.next())
            {
                setVisible(false);
                Account ob=new Account();
                ob.setVisible(true);
                Account.jLabel2.setText(rs.getString(1));
                Account.jLabel3.setText(rs.getString(2));
                Account.jLabel8.setText(rs.getString(3));
                Account.jLabel9.setText(rs.getString(4));
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Invalid roll no or password!");
                jTextField1.setText("");
                jTextField2.setText("");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

