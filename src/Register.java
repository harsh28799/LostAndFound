    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String roll=jTextField1.getText();
            String name=jTextField2.getText();
            String address=jTextField3.getText()+","+jComboBox1.getSelectedItem();
            String contact=jTextField4.getText();
            String password=jPasswordField1.getPassword()+"";
            
            String chk="SELECT * FROM REGISTER WHERE ROLL='"+roll+"'";
            pat=conn.prepareStatement(chk);
            rs=pat.executeQuery();
            
            if(rs.next())
                    JOptionPane.showMessageDialog(null,"User Already Registered!");
            else if(roll.equals("")||name.equals("")||contact.equals("")||address.equals("")||password.equals(""))
                    JOptionPane.showMessageDialog(null,"All FIELD NECESSARY");
            else if(contact.length()!=10)
                JOptionPane.showMessageDialog(null,"ENTER A VALID CONTACT");
            else 
            {
                String q="INSERT INTO REGISTER VALUES (?,?,?,?,?)";
                pat=conn.prepareStatement(q);
                pat.setString(1,roll);
                pat.setString(2,name);
                pat.setString(3,address);
                pat.setString(4,contact);
                pat.setString(5,password);
                pat.execute();
                JOptionPane.showMessageDialog(null,"NEW USER INSERTED");
                pat.close();
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jPasswordField1.setText("");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }        
    }//GEN-LAST:event_jButton1ActionPerformed

